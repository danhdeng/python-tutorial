# Basic Arithmetic
x = 30
y = 40
z= x + y

print(f"x is {x}, y is {y}, x + y is {z}")
print(f"y-x : {y-x}")
print(f"y*x : {y*x}")

# Division
print(f"y/x : {y/x}")

# Floor division
print(f"y//x : {y//x}")

# Modulo operator - get the remainder
remainder = 9 % 2
print(f"remainder : {remainder}")

# Exponent
four_square =4**2
print(f"four_square : {four_square}")  # 16


